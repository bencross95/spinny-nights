document.getElementById("article-1").addEventListener("mouseover", function() {
  document.body.style.backgroundImage = "url(images/07.png)";
  document.body.style.backgroundColor = "#000";
  document.getElementById("article-1").style.color = "#FFFFFF";
  document.getElementById("date-1").style.color = "#FFFFFF";
  document.getElementById("event-name-1").style.color = "#FFFFFF";
  document.getElementById("manicule-1").style.color = "#FFFFFF";
  document.getElementById("article-1-ticket").style.color = "#FFFFFF";

}, false);


document.getElementById("article-1").addEventListener("mouseout", function() {
   document.body.style.backgroundImage = "";
  document.getElementById("article-1").style.color = ""
  document.body.style.backgroundColor = "";
  document.getElementById("date-1").style.color = "";
  document.getElementById("event-name-1").style.color = "";
  document.getElementById("manicule-1").style.color = "";
  document.getElementById("article-1-ticket").style.color = "";
}, false);



